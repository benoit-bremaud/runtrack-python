# Boucle While
print("\nBoucle WHILE")
numb = 0
while numb <= 20:
    print(numb)
    numb +=2
else:
    print("\n\tFini !!")
    

# Boucle avec fonction RANGE + la boucle FOR
print("\nBoucle avec FOR + RANGE")
for numb in range(0,21,2):  # range(0,21,2) -> 0=départ, 21=arrivée, 2=incrément !
    print(numb)
else:
    print("\n\tFinished !!")